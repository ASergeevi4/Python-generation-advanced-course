my_input = [i.upper() for i in input()]
my_dict = {1: ('.', ',', '?', '!', ':'),
           2: ('A', 'B', 'C'),
           3: ('D', 'E', 'F'),
           4: ('G', 'H', 'I'),
           5: ('J', 'K', 'L'),
           6: ('M', 'N', 'O'),
           7: ('P', 'Q', 'R', 'S'),
           8: ('T', 'U', 'V'),
           9: ('W', 'X', 'Y', 'Z'),
           0: ' '}
result = []
for i in my_input:
    for key, value in my_dict.items():
        if i in value:
            adding = str(key) * (value.index(i) + 1)
            result.append(adding)

print(''.join(result))
