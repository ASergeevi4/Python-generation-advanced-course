# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 3. –¢–µ–ª–µ—Ñ–æ–Ω ‚Äî —ç—Ç–æ –Ω–µ—Å–∫–æ–ª—å–∫–æ —Ü–∏—Ñ—Ä, –∑–∞–ø–∏—Å–∞–Ω–Ω—ã—Ö –ø–æ–¥—Ä—è–¥, –∞ –∏–º—è –º–æ–∂–µ—Ç —Å–æ—Å—Ç–æ—è—Ç—å –∏–∑ –±—É–∫–≤ —Ä—É—Å—Å–∫–æ–≥–æ –∏–ª–∏ –∞–Ω–≥–ª–∏–π—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞. –ó–∞–ø–∏—Å–∏ –Ω–µ –ø–æ–≤—Ç–æ—Ä—è—é—Ç—Å—è.

list_of_insert = [[i for i in input().lower().split()] for _ in range(int(input()))]
phone_book = {}
for el in list_of_insert:
    name, phone = el[1], el[0]
    if name in phone_book:
        phone_book[name].append(phone)
    else:
        phone_book[name] = [phone]

for _ in range(int(input())):
    print(*phone_book.get(input().lower(), ['–∞–±–æ–Ω–µ–Ω—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω']))


