def read_csv():
    with open('data.csv', 'r', encoding='utf-8') as file:
        keys = [el.strip() for el in file.readline().split(',')]
        values_list = [el.strip().split(',') for el in file.readlines()]
        result = [{keys[i]: values[i] for i in range(len(keys))} for values in values_list]
        return result
